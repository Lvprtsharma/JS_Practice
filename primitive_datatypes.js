/*

Primitive Data Types:
1. Number
2. String
3. Boolean
4. Null
5. Undefined
6. Symbol
7. BigInt

*/

// Number
let agenum = 20;
console.log(agenum);
console.log(typeof agenum);

// String
let fullname = "Lovepreet";
console.log(fullname);
console.log(typeof fullname);

// Boolean
let isStudent = true;
console.log(isStudent);
console.log(typeof isStudent);

// Null
let a = null;
console.log(a);
console.log(typeof a);

// Undefined
let b;
console.log(b);
console.log(typeof b);

// Symbol
let c = Symbol("Lovepreet");
console.log(c);
console.log(typeof c);

// BigInt
let d = 123456789012345678901234567890n;
console.log(d);
console.log(typeof d);

